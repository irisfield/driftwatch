import { type RetrievalReportDelta } from "./compare.js";
import { type RetrievalReport } from "./evaluate.js";

export type DriftFailureKind =
  | "corpus-changed"
  | "embedding-model-changed"
  | "retriever-regressed"
  | "per-query-floor-failed";

export class DriftGateError extends Error {
  readonly kind: DriftFailureKind;

  constructor(kind: DriftFailureKind, message: string) {
    super(message);
    this.name = "DriftGateError";
    this.kind = kind;
  }
}

export interface HealthThresholds {
  minRecallAtK?: number;
  minPrecisionAtK?: number;
  minMrr?: number;
  minNdcgAtK?: number;
  minHitRate?: number;
}

// Only gate metrics have defaults in calibration; MRR/nDCG thresholds are
// accepted but should only be set deliberately, not by the calibration step
export interface RegressionThresholds {
  maxRecallDrop?: number;
  maxHitRateDrop?: number;
  // Diagnostic — present for completeness, not generated by calibration
  maxPrecisionDrop?: number;
  maxMrrDrop?: number;
  maxNdcgDrop?: number;
}

// Per-query floors: "no query may REGRESS TO below this value from a non-zero baseline"
// Not "no query may BE below this value" — that would permanently fail hard queries
export interface PerQueryFloors {
  minHitRate?: number;
  minRecallAtK?: number;
}

function fmt(n: number): string {
  return n.toFixed(4);
}

export function assertRetrievalHealthy(
  report: RetrievalReport,
  thresholds: HealthThresholds,
): void {
  const violations: string[] = [];

  if (thresholds.minRecallAtK !== undefined && report.recallAtK < thresholds.minRecallAtK) {
    violations.push(
      `  recallAtK: ${fmt(report.recallAtK)} < minimum ${fmt(thresholds.minRecallAtK)}`,
    );
  }
  if (
    thresholds.minPrecisionAtK !== undefined &&
    report.precisionAtK < thresholds.minPrecisionAtK
  ) {
    violations.push(
      `  precisionAtK: ${fmt(report.precisionAtK)} < minimum ${fmt(thresholds.minPrecisionAtK)}`,
    );
  }
  if (thresholds.minMrr !== undefined && report.mrr < thresholds.minMrr) {
    violations.push(`  mrr: ${fmt(report.mrr)} < minimum ${fmt(thresholds.minMrr)}`);
  }
  if (thresholds.minNdcgAtK !== undefined && report.ndcgAtK < thresholds.minNdcgAtK) {
    violations.push(`  ndcgAtK: ${fmt(report.ndcgAtK)} < minimum ${fmt(thresholds.minNdcgAtK)}`);
  }
  if (thresholds.minHitRate !== undefined && report.hitRate < thresholds.minHitRate) {
    violations.push(`  hitRate: ${fmt(report.hitRate)} < minimum ${fmt(thresholds.minHitRate)}`);
  }

  if (violations.length > 0) {
    throw new DriftGateError(
      "retriever-regressed",
      `Retrieval health check failed:\n${violations.join("\n")}`,
    );
  }
}

export function assertNoRegression(
  delta: RetrievalReportDelta,
  thresholds: RegressionThresholds,
): void {
  // Priority 1: embedding model swap — never misreport as retriever regression
  if (delta.fingerprint.embeddingModelChanged) {
    throw new DriftGateError(
      "embedding-model-changed",
      "Embedding model changed since baseline. Regenerate the baseline against the new model.",
    );
  }

  // Priority 2: corpus content changed — re-baseline required
  if (delta.fingerprint.corpusChanged) {
    throw new DriftGateError(
      "corpus-changed",
      "Corpus content changed since baseline. Re-run scripts/run-eval.ts to regenerate the baseline, or investigate the corpus change.",
    );
  }

  // Priority 3: fingerprint absent — warn but continue conservatively
  if (!delta.fingerprint.comparable) {
    console.warn(
      "[driftwatch] Corpus fingerprint absent from one or both reports. " +
        "Cannot classify drift source. Proceeding with metric check.",
    );
  }

  // Priority 4: mean metric check
  const violations: string[] = [];

  function check(
    name: string,
    d: { before: number; after: number; delta: number },
    maxDrop: number | undefined,
  ): void {
    if (maxDrop !== undefined && d.delta < -maxDrop) {
      violations.push(
        `  ${name} dropped ${fmt(-d.delta)} (limit: ${fmt(maxDrop)}) — before: ${fmt(d.before)}, after: ${fmt(d.after)}`,
      );
    }
  }

  check("recallAtK", delta.recallAtK, thresholds.maxRecallDrop);
  check("hitRate", delta.hitRate, thresholds.maxHitRateDrop);
  check("precisionAtK", delta.precisionAtK, thresholds.maxPrecisionDrop);
  check("mrr", delta.mrr, thresholds.maxMrrDrop);
  check("ndcgAtK", delta.ndcgAtK, thresholds.maxNdcgDrop);

  if (violations.length > 0) {
    const userCount = delta.queries.filter((q) => q.source === "user").length;
    const syntheticCount = delta.queries.filter((q) => q.source === "synthetic").length;
    const goldenLine = `Golden set: ${String(delta.queries.length)} queries (${String(userCount)} user, ${String(syntheticCount)} synthetic)`;
    throw new DriftGateError(
      "retriever-regressed",
      `Retrieval regression detected:\n${violations.join("\n")}\n${goldenLine}`,
    );
  }
}

export function assertPerQueryFloors(delta: RetrievalReportDelta, floors: PerQueryFloors): void {
  const violations: string[] = [];
  const failingQueries = new Set<string>();

  for (const queryDelta of delta.queries) {
    const sourceTag = queryDelta.source === undefined ? "" : ` [source: ${queryDelta.source}]`;

    if (
      floors.minHitRate !== undefined &&
      queryDelta.hitRate.before > 0 &&
      queryDelta.hitRate.after < floors.minHitRate
    ) {
      failingQueries.add(queryDelta.query);
      violations.push(
        `  "${queryDelta.query}" — hitRate: ${fmt(queryDelta.hitRate.before)} → ${fmt(queryDelta.hitRate.after)}${sourceTag}`,
      );
    }

    if (
      floors.minRecallAtK !== undefined &&
      queryDelta.recallAtK.before > 0 &&
      queryDelta.recallAtK.after < floors.minRecallAtK
    ) {
      failingQueries.add(queryDelta.query);
      violations.push(
        `  "${queryDelta.query}" — recallAtK: ${fmt(queryDelta.recallAtK.before)} → ${fmt(queryDelta.recallAtK.after)}${sourceTag}`,
      );
    }
  }

  if (violations.length > 0) {
    throw new DriftGateError(
      "per-query-floor-failed",
      `Per-query floor failed on ${String(failingQueries.size)} queries:\n${violations.join("\n")}`,
    );
  }
}
